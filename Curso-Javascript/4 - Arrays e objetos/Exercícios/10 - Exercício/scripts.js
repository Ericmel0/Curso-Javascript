let calculadora = {
  somar: function (a, b) {
    console.log(a + b);
  },
  subtrair: function (a, b) {
    console.log(a - b);
  },
  multiplicar: function (a, b) {
    console.log(a * b);
  },
  dividir: function (a, b) {
    console.log(a / b);
  },
};

calculadora.somar(3, 5);
calculadora.subtrair(3, 5);
calculadora.multiplicar(3, 5);
calculadora.dividir(3, 5);
